# List the four events that are occurring when these statements are executed.
list1 = []
print (list1)
list1.append("value")
print (list1)
list1.insert(1, 10)
print (list1)
list1.pop(0)
print (list1)
# your answer:

# “list1 = []” declares a list data type value and assigns it to list1 variable

# “list1.append("value")” adds the string data type “value” to the end of list1

# “list1.insert(1, 10)” inserts the number data type 10 into index 1 of list1

# “list1.pop(0)” removes the element at index 0 of list1 (in this case the string “value”)

# “print(list1)” prints the list to the console (in this case [10] will be printed)
